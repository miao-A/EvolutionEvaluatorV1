package cn.seu.edu.complexityevaluator.util;

public enum ChangeTypes  {
	//类中属性的变更
	REMOVED_OBJECT_STATE,//类中属性的删除
	ADDITIONAL_OBJECT_STATE,//类中属性的增加
	ADDING_ATTRIBUTE_MODIFIABILITY,//成员变量删除final修饰
	REMOVING_ATTRIBUTE_MODIFIABILITY,//成员变量增加final修饰；
	//类中声明的变更
	ADDING_CLASS_DERIVABILITY,//类声明中去除fianl
	REMOVING_CLASS_DERIVABILITY,//类声明中加入final
	PARENT_CLASS_DELETE,//父类删除
	PARENT_CLASS_INSERT,//父类的增加
	PARENT_INTERFACE_DELETE,//接口删除
	PARENT_INTERFACE_INSERT,//接口插入
	//方法体内语句的变更
	STATEMENT_DELETE,//语句的删除
	STATEMENT_INSERT,//插入语句
	//方法声明变更
	PARAMETER_DELETE,//方法参数的删除
	PARAMETER_INSERT,//方法参数的插入
	ADDING_METHOD_OVERRIDABILITY,//删除方法的final修饰符
	REMOVING_METHOD_OVERRIDABILITY,//增加final修饰符
	//方法的增加和删除
	REMOVED_FUNCTIONALITY,//删除的方法
	ADDITIONAL_FUNCTIONALITY,//增加的方法
	//可能有影响的变更
	CONDITION_EXPRESSION_CHANGE,//方法条件表达式的变更
	STATEMENT_UPDATE,//方法语句的变更
	DECREASING_ACCESSIBILITY_CHANGE,//降低可见性，如public到private
	INCREASING_ACCESSIBILITY_CHANGE,//提高可见性
	UNCLASSIFIED_CHANGE,//未分类的修改
	//else语句的增删
	ALTERNATIVE_PART_DELETE,
	ALTERNATIVE_PART_INSERT,
	}

